let Jalan = "Up";

switch (Jalan) {

    case "Up":
        console.log("Jalan ke atas");
        break;
    case "Right":
        console.log("Jalan ke kanan");
        break;
    case "Left":
        console.log("Jalan ke kiri");
        break;
    case "Bottom":
        console.log("Jalan ke bawah");
        break;
    default :
    console.log("Jalan ke segala arah");

}